    private void Generar() throws DocumentException, IOException {
        String tipo = cbTid.getSelectedItem().toString();
        String ruta = txtURL.getText().trim();
        
        if(tipo.equals("Acta")){
           GeneracionActas gen = new GeneracionActas();
            Map<String, String> respuestaerr = gen.GenerarActas(ruta);
            
            if(respuestaerr.get("ESTADO").equals("ERROR")){
            JOptionPane.showMessageDialog(this, "posible error de digitación "+Utilidades.Utilidades.decodificarElemento(respuestaerr.get("MENSAJE"))+
                    "\nPara el docente: "+Utilidades.Utilidades.decodificarElemento(respuestaerr.get("LINEA_ERROR_DOCENTE"))+
                    "\nTipo de producto: "+Utilidades.Utilidades.decodificarElemento(respuestaerr.get("LINEA_ERROR_PRODUCTO"))+"\nEn el producto: "+Utilidades.Utilidades.decodificarElemento(respuestaerr.get("NOMBRE_PRODUCTO")),
                    respuestaerr.get("ESTADO"), JOptionPane.DEFAULT_OPTION);
            }else if(respuestaerr.get("ESTADO").equals("OK")){
                JOptionPane.showMessageDialog(this,"EJECUCION EXITOSA", respuestaerr.get("ESTADO"), JOptionPane.DEFAULT_OPTION);
            }
        } else if(tipo.equals("Resolucion")){
            String semestre = ""+cbSemestre.getSelectedItem();
            if(semestre.equals("Seleccionar")){
                JOptionPane.showMessageDialog(this, "Por favor seleccione un semestre");
                return;
            }
            GeneracionResoluciones gen = new GeneracionResoluciones();
            Map<String, String> respuesta = gen.CrearResoluciones(ruta, semestre);
            
            if(respuesta.get("ESTADO").equals("ERROR")){
            JOptionPane.showMessageDialog(this, "posible error de digitación "+Utilidades.Utilidades.decodificarElemento(respuesta.get("MENSAJE"))+
                    "\nPara el docente: "+Utilidades.Utilidades.decodificarElemento(respuesta.get("LINEA_ERROR_DOCENTE"))+
                    "\nTipo de producto: "+Utilidades.Utilidades.decodificarElemento(respuesta.get("LINEA_ERROR_PRODUCTO"))+"\nEn el producto: "+Utilidades.Utilidades.decodificarElemento(respuesta.get("NOMBRE_PRODUCTO")),
                    respuesta.get("ESTADO"), JOptionPane.DEFAULT_OPTION);
            }else if(respuesta.get("ESTADO").equals("OK")){
                JOptionPane.showMessageDialog(this,"EJECUCION EXITOSA", respuesta.get("ESTADO"), JOptionPane.DEFAULT_OPTION);
            }
           
        } 
    }


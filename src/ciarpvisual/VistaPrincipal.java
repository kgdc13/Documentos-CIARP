    private void GenerarCuadros()throws DocumentException, IOException {
        String anio = cbAnio.getSelectedItem().toString();
        String rutaDocentes = txtURLDocentes.getText().trim();
        String rutaPuntos = txtURLPuntos.getText().trim();
        
        
        if(anio.equals("Seleccionar")){
            JOptionPane.showMessageDialog(this, "Por favor seleccione un año.");
            return;
        }
        
        if(rutaDocentes.equals("")){
            JOptionPane.showMessageDialog(this, "Por favor seleccione el archivo de los docentes de planta.");
            return;
        }
        
        if(rutaPuntos.equals("")){
            JOptionPane.showMessageDialog(this, "Por favor seleccione el archivo de puntos todos.");
            return;
        }
        
        GeneracionCuadros gen = new GeneracionCuadros();
        Map<String, String> respuesta = gen.GenerarCuadros(anio, rutaDocentes, rutaPuntos);
        if(respuesta.get("ESTADO").equals("ERROR")){
        JOptionPane.showMessageDialog(this, "posible error de digitación "+Utilidades.Utilidades.decodificarElemento(respuesta.get("MENSAJE"))+"\nPara el docente: "+Utilidades.Utilidades.decodificarElemento(respuesta.get("LINEA_ERROR_DOCENTE"))+
                "\nTipo de producto: "+Utilidades.Utilidades.decodificarElemento(respuesta.get("LINEA_ERROR_PRODUCTO"))+
                "\nEn el producto: "+Utilidades.Utilidades.decodificarElemento(respuesta.get("NOMBRE_SOLICITUD")), respuesta.get("ESTADO"), JOptionPane.DEFAULT_OPTION);
        }else if(respuesta.get("ESTADO").equals("OK")){
            JOptionPane.showMessageDialog(this, "EJECUCION EXITOSA", respuesta.get("ESTADO"), JOptionPane.DEFAULT_OPTION);
        }
    }

    private void GenerarCartas() throws DocumentException, IOException, Exception {
       String consecutivo = txtConsecutivo.getText().trim();
       String anio = lblAnio.getText().trim();
       String rutaOrden = txtOrdendeldia.getText().trim();
       if(rutaOrden.equals("")){
            JOptionPane.showMessageDialog(this, "Por favor seleccione el archivo de orden del día.");
            return;
        }
       GeneracionCartas gen = new GeneracionCartas();
       Map<String, String> respuesta = gen.Generarcartas(rutaOrden, consecutivo, anio);
       if(respuesta.get("ESTADO").equals("ERROR")){
       JOptionPane.showMessageDialog(this, "Posible error de digitación "+Utilidades.Utilidades.decodificarElemento(respuesta.get("MENSAJE"))
               +"\nPara el docente: "+Utilidades.Utilidades.decodificarElemento(respuesta.get("LINEA_ERROR_DOCENTE"))+
               "\nTipo de producto: "+Utilidades.Utilidades.decodificarElemento(respuesta.get("LINEA_ERROR_PRODUCTO"))+"\nEn el producto: "+Utilidades.Utilidades.decodificarElemento(respuesta.get("NOMBRE_PRODUCTO")), respuesta.get("ESTADO"),JOptionPane.DEFAULT_OPTION);
        }else if(respuesta.get("ESTADO").equals("OK")){
            JOptionPane.showMessageDialog(this, "EJECUCION EXITOSA", respuesta.get("ESTADO"),JOptionPane.DEFAULT_OPTION);
        }
    }   
    
